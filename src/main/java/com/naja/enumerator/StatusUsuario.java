package com.naja.enumerator;

import java.io.Serializable;

/**
 *
 * @author Lemoel Marques lemoel@gmail.com NajaSoftware
 */
public enum StatusUsuario implements Serializable {

    Ativo("ATIVO"),
    Inativo("INATIVO");
    
    
    private String nome;

    StatusUsuario(String nome) {
        this.nome = nome;
    }

    public String getDescricao() {
        return nome;
    }

}
